package day4;

public class TypeCastingDemo {
public static void main(String[] args) {
	//implicit type casting(conversion)-- done by compiler 

	byte b=4;
	
	int i=20;
	
	System.out.println(b+"  "+i);
	
	
	
	
	//explicit type casting(conversion)--done by compiler
int a=500;
byte c=(byte)a;

System.out.println(a+"  "+c );


}
}
